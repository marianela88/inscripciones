export class Escuela{
    id: number;
    tipo: null;
    categoria: string;
    cue: number;
    nombre: string;
    domicilio: string;
    turno: string;
    localidad: string;
    porcentaje_zona: number;
    zona: string;

    constructor(){
        this.id = this.id
        this.tipo = this.tipo
        this.categoria = this.categoria
        this.cue = this.cue
        this.nombre = this.nombre
        this.domicilio = this.domicilio
        this.turno = this.turno
        this.localidad = this.localidad
        this.porcentaje_zona = this.porcentaje_zona
        this.zona = this.zona
    }
}